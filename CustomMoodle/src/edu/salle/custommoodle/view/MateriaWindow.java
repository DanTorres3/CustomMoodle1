    public void commitChanges(List<Materia> materiaList) throws IOException
    {
        try
        {
            Gson gson = new Gson();
            FileWriter fw = new FileWriter("materia.json");
            fw.write(gson.toJson(materiaList));
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }
    }
    
    public void load ()
    {
        try
        {
            Gson gson = new Gson();
            BufferedReader br = new BufferedReader(new FileReader("materia.json"));
            materiaList = gson.fromJson(br, new TypeToken<List<Materia>>(){}.getType());
            if(materiaList==null)
            {
                materiaList = new ArrayList<>();
            }
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
        }
    }
    
