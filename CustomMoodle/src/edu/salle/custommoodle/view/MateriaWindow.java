    public void actualizar(Materia student)
    {
        int pos = 9;
        if(student.getNombre().toLowerCase().equals("matemáticas"))
            pos=0;
        else if(student.getNombre().toLowerCase().equals("computación"))
            pos=1;
        else
            pos=2;
        materiaList.set(pos, student);
    }
    
    public void clear()
    {
        DefaultTableModel dtm = (DefaultTableModel)tMateria.getModel();
        while(dtm.getRowCount()>0)
            dtm.removeRow(0);
    }
    
    public void refresh(List<Materia> materiaList)
    {
        clear();
        DefaultTableModel dtm = (DefaultTableModel)tMateria.getModel();
        Object[] emptyRow = {""};
        
        for (int i = 0; i < materiaList.size() ; i++) {
            dtm.addRow(emptyRow);
            dtm.setValueAt(materiaList.get(i).getNombre(), i, 0); //Valor a insertar, , columna
            dtm.setValueAt(materiaList.get(i).getDocente(), i, 1);
        }
        
    }
